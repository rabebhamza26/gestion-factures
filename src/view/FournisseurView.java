    private void jButtonADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonADDActionPerformed
        // TODO add your handling code here:
       
    // Ajouter un nouveau fournisseur
    Fournisseur fournisseur = new Fournisseur();
    fournisseur.setNomFour(jTextFieldNomFour.getText());
    fournisseur.setAdresseFour(jTextFieldAdresseFour.getText());
    fournisseur.setContactFour(jTextFieldContactFour.getText());

    FournisseurController fc = new FournisseurController();
    if (fc.create(fournisseur)) {
        JOptionPane.showMessageDialog(this, "Fournisseur ajouté avec succès !", "Message d'information", JOptionPane.INFORMATION_MESSAGE);
        new FournisseurView().setVisible(true);
        this.setVisible(false);    
    } else {
        JOptionPane.showMessageDialog(this, "Échec de l'ajout du fournisseur ! Veuillez réessayer.", "Message d'erreur", JOptionPane.ERROR_MESSAGE);
    }

    // Réinitialiser les champs de saisie
    

    jTextFieldNomFour.setText("");
    jTextFieldAdresseFour.setText("");
    jTextFieldContactFour.setText("");

    }//GEN-LAST:event_jButtonADDActionPerformed

    private void jButtonDeletActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletActionPerformed
        // Vérifier si une ligne est sélectionnée
    int selectedRow = jTableListOfFour.getSelectedRow();
    if (selectedRow >= 0) {
        // Récupérer l'ID du fournisseur à partir de la table
        int fournisseurId = Integer.parseInt(jTableListOfFour.getValueAt(selectedRow, 0).toString());
        
        // Créer une instance du contrôleur fournisseur
        FournisseurController fournisseurController = new FournisseurController();

        // Tenter de supprimer le fournisseur
        if (fournisseurController.delete(fournisseurId)) {
            // Si la suppression est réussie, supprimer la ligne de la table
            ((DefaultTableModel) jTableListOfFour.getModel()).removeRow(selectedRow);
            JOptionPane.showMessageDialog(this, "Fournisseur supprimé avec succès !");
        } else {
            // Afficher un message d'erreur si la suppression échoue
            JOptionPane.showMessageDialog(this, "Erreur lors de la suppression du fournisseur.");
        }
    } else {
        // Afficher un message si aucune ligne n'est sélectionnée
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner une ligne à supprimer !");
    }
       
        
    }//GEN-LAST:event_jButtonDeletActionPerformed

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // TODO add your handling code here:2
    int selectedRow = jTableListOfFour.getSelectedRow();
    if (selectedRow >= 0) {
        String newNom = jTextFieldNomFour.getText();
        String newAdresse = jTextFieldAdresseFour.getText();
        String newContact = jTextFieldContactFour.getText();

        int fournisseurId = Integer.parseInt(jTableListOfFour.getValueAt(selectedRow, 0).toString());
        Fournisseur fournisseur = new Fournisseur(fournisseurId, newNom, newAdresse, newContact);
        FournisseurController fournisseurController = new FournisseurController();

        if (fournisseurController.update(fournisseur, fournisseurId)) {
            jTableListOfFour.setValueAt(newNom, selectedRow, 1);
            jTableListOfFour.setValueAt(newAdresse, selectedRow, 2);
            jTableListOfFour.setValueAt(newContact, selectedRow, 3);

            JOptionPane.showMessageDialog(this, "Fournisseur mis à jour avec succès !");
        } else {
            JOptionPane.showMessageDialog(this, "Erreur lors de la mise à jour du fournisseur.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner une ligne à modifier !");
    }
        
    }//GEN-LAST:event_jButtonEditActionPerformed

