    private void jButtonAfficherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAfficherActionPerformed
        // TODO add your handling code here:
        // Retrieve data from the database for fournisseurs:
FournisseurController fc = new FournisseurController(); // Assume you have a similar controller for fournisseurs
ArrayList<Fournisseur> fournisseurs = fc.getAll();

// Set up the table model for fournisseurs:
DefaultTableModel model = new DefaultTableModel();
model.addColumn("Id");
model.addColumn("Nom");
model.addColumn("Adresse");
model.addColumn("Contact");
jTableListOfFour.setModel(model);

// Populate the table with fournisseur data:
for (Fournisseur f : fournisseurs) {
    Object[] row = new Object[]{
        f.getIdFour(), 
        f.getNomFour(), 
        f.getAdresseFour(), 
        f.getContactFour(), 
    };
    model.addRow(row);
}

// Adjust the table appearance:
jTableListOfFour.setRowHeight(25);
jTableListOfFour.setModel(model);
    }//GEN-LAST:event_jButtonAfficherActionPerformed

